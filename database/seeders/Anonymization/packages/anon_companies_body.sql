  -- Deterministic lookup by seed
  FUNCTION GET_COMPANIES(
    p_seed IN VARCHAR2,
    p_max_len IN NUMBER DEFAULT 100
  ) RETURN VARCHAR2 DETERMINISTIC IS
    v_hash RAW(32);
    v_idx NUMBER;
  BEGIN
    v_hash := DBMS_CRYPTO.HASH(
      UTL_RAW.CAST_TO_RAW(NVL(p_seed, 'NULL')),
      DBMS_CRYPTO.HASH_SH256
    );
    v_idx := MOD(TO_NUMBER(SUBSTR(RAWTOHEX(v_hash), 1, 8), 'XXXXXXXX'), C_COUNT) + 1;
    RETURN SUBSTR(g_data(v_idx), 1, LEAST(p_max_len, 100));
  END GET_COMPANIES;

  -- Random lookup (non-deterministic)
  FUNCTION GET_COMPANIES_RANDOM(
    p_max_len IN NUMBER DEFAULT 100
  ) RETURN VARCHAR2 IS
    v_idx NUMBER;
  BEGIN
    v_idx := TRUNC(DBMS_RANDOM.VALUE(1, C_COUNT + 1));
    RETURN SUBSTR(g_data(v_idx), 1, LEAST(p_max_len, 100));
  END GET_COMPANIES_RANDOM;

  -- Index-based lookup
  FUNCTION GET_COMPANIES_BY_INDEX(
    p_index IN NUMBER,
    p_max_len IN NUMBER DEFAULT 100
  ) RETURN VARCHAR2 DETERMINISTIC IS
    v_idx NUMBER;
